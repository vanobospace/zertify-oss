const currentLocale = ref<PublicLocale>(DEFAULT_PUBLIC_LOCALE);
let initialized = false;

const resolveMessage = (locale: PublicLocale, key: string): string => {
    return translations[locale][key] ?? translations.de[key] ?? key;
};

const applyDocumentLocale = (locale: PublicLocale): void => {
    if (typeof document === 'undefined') {
        return;
    }

    const direction = RTL_LOCALES.has(locale) ? 'rtl' : 'ltr';

    document.documentElement.lang = locale;
    document.documentElement.dir = direction;
    document.body.lang = locale;
    document.body.dir = direction;
};

const initialize = (): void => {
    if (initialized) {
        return;
    }

    if (typeof window !== 'undefined') {
        const stored = window.localStorage.getItem(STORAGE_KEY);

        if (stored && localeOptions.some((option) => option.code === stored)) {
            currentLocale.value = stored as PublicLocale;
        } else {
            currentLocale.value = detectPreferredPublicLocale([
                ...(window.navigator.languages ?? []),
                window.navigator.language,
            ]);
        }
    }

    applyDocumentLocale(currentLocale.value);
    initialized = true;
};

export const usePublicLocale = () => {
    initialize();

    const isRtl = computed(() => RTL_LOCALES.has(currentLocale.value));
    const direction = computed(() => (isRtl.value ? 'rtl' : 'ltr'));

    const setLocale = (locale: PublicLocale): void => {
        currentLocale.value = locale;

        if (typeof window !== 'undefined') {
            window.localStorage.setItem(STORAGE_KEY, locale);
        }

        applyDocumentLocale(locale);
    };

    const t = (key: string): string => resolveMessage(currentLocale.value, key);

    const tr = (
        key: string,
        params: Record<string, string | number>,
    ): string => {
        let text = t(key);

        for (const [paramKey, value] of Object.entries(params)) {
            text = text.replaceAll(`:${paramKey}`, String(value));
        }

        return text;
    };

    return {
        currentLocale,
        localeOptions,
        setLocale,
        t,
        tr,
        isRtl,
        direction,
    };
};
